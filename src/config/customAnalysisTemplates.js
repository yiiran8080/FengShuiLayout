// 獲取特定元素和關注領域的分析
export function getCustomAnalysis(element, concern, specificType = "通用") {
	const config = ANALYSIS_CONFIG.concerns[concern];
	if (!config) return null;

	return {
		intro: config.intro[Math.floor(Math.random() * config.intro.length)],
		traits: config.traits[element]
			? config.traits[element][
					Math.floor(Math.random() * config.traits[element].length)
				]
			: config.traits["通用"],
		forecast: config.forecast?.[element]
			? config.forecast[element][
					Math.floor(Math.random() * config.forecast[element].length)
				]
			: null,
		advice: config.advice[specificType] || config.advice["通用"],
		fengshui: config.fengshui,
	};
}

// 獲取合婚分析
export function getCustomCoupleAnalysis(userElement, partnerElement) {
	const compatibility =
		COUPLE_ANALYSIS_CONFIG.compatibility[userElement]?.[partnerElement] ||
		60;

	let level;
	if (compatibility >= 80) level = "高相性";
	else if (compatibility >= 60) level = "中相性";
	else level = "低相性";

	const template = COUPLE_ANALYSIS_CONFIG.analysisTemplates[level];
	const analysis = template(userElement, partnerElement, compatibility);

	return {
		userElement,
		partnerElement,
		compatibility,
		...analysis,
		fengShuiAdvice: COUPLE_ANALYSIS_CONFIG.fengShuiAdvice,
	};
}

